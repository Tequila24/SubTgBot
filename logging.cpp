#include "logging.h"

// ================================================================================================================================
Log::Log()
{
    // disable buffering for stdout
    setvbuf(stdout, NULL, _IONBF, 0);
}
// ================================================================================================================================
Log::~Log()
{}
// ================================================================================================================================
void Log::logCL(std::string line)
{
    std::cout << line << std::endl;
}
